    fun getMaterial(mate: String?): Material {
        return try {
            Material.valueOf(mate!!)
        } catch (ignore2: IllegalArgumentException) {
            // try next
            Material.AIR
        }
    }

    fun getMaterial(vararg mates: String): Material? {
        for (i in mates.indices) {
            val mate = mates[i].trim { it <= ' ' }
            return try {
                Material.valueOf(mate)
            } catch (ignore2: IllegalArgumentException) {
                // try next
                continue
            }
        }
        return Material.AIR
    }


    fun isMaterial(material: Material, vararg mates: String): Boolean {
        for (i in mates.indices) {
            val mate = mates[i].trim { it <= ' ' }
            return if (material == getMaterial(mate)) {
                true
            } else {
                continue
            }
        }
        return false
    }

